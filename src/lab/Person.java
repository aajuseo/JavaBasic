package lab;

class Person {
    String name;
    String job;
    int age;
    String gender;
    String blood;

    public Person(String name, String job, int age,
                  String gender, String blood) {
        this.name = name;
        this.job = job;
        this.age = age;
        this.gender = gender;
        this.blood = blood;
    }
}